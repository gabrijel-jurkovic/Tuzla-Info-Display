//function to show map
function prikaziMapo() {
    
            mapOptions = {
                center: positionInfo,
                zoom: 15.2
            };
            mapa = L.map('mapa_id', mapOptions);
            var layer = new L.TileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png');
            mapa.addLayer(layer);
            startMarker = L.marker([positionInfo.lat, positionInfo.lng], {icon: ikona}).addTo(mapa);
            mapa.on('click',function(){
                
                if(pot==null){
                    
                 enable=false;
                 enableGoButton(); 
                 }
             
             })
        
    
    
}

