            for(var i=0;i<markerjiRestorani.length;i++){
                mapa.removeLayer(markerjiRestorani[i]);
            }
            for(var i=0;i<markerjiKafane.length;i++){
                mapa.removeLayer(markerjiKafane[i]);
            }
            
